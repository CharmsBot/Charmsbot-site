  // ─── Estado ───────────────────────────────────────────────
  let currentLang = localStorage.getItem('charmsbot_lang') || 'en';

  // ─── API pública ──────────────────────────────────────────
  window.applyHeaderTranslations = function() { applyTranslations(); };

  window.t = function (key, fallback) {
    return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key] !== undefined)
      ? TRANSLATIONS[currentLang][key]
      : (fallback !== undefined ? fallback : key);
  };

  window.setLang = function (lang) {
    if (!TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem('charmsbot_lang', lang);
    applyTranslations();
    updateLangButtons();
    window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  };

  window.getCurrentLang = function () { return currentLang; };

  // ─── Motor ────────────────────────────────────────────────
  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = window.t(key);
      if (val && val !== key) el.innerHTML = val;
    });
    document.documentElement.lang = currentLang;
  }

  function updateLangButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === currentLang);
    });
  }

  function init() {
    applyTranslations();
    updateLangButtons();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

